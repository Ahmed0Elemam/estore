<p class='ar'>اختتمت اليوم فعاليات القافلة المائية المائية لشركة مياه الشرب والصرف الصحي بالدقهلية، والتي استمرت على مدار يومان بقرية منية سندوب، ونظمتها الإدارة العامة للعلاقات العامة والتوعية بالشركة لحل مشكلات القرية التي تخص مياه الشرب والصرف الصحي، وكذلك المشكلات الخاصة بالمحاسبة، هذا وقد حرص قيادات الشركة من مختلف القطاعات على التواجد خلال القافلة لمتابعة العمل، والتواصل مع المواطنين بشكل مباشر، بناءًا على توجيهات السيد المهندس/ عزت الصياد &ndash; رئيس مجلس الإدارة والعضو المنتدب.<br /> وقد تضمنت الفعاليات إقامة ندوات لطلاب المدارس، ورياض الأطفال بالمقر الثابت للقافلة في جمعية تنمية المجتمع المحلي بمنية سندوب، وذلك لتوعيتهم بأهمية المياه، كما تم عرض أفلام كارتون عن ترشيد المياه، وتم عمل ورش تلوين للأطفال. كما تم توزيع هدايا تذكارية على الأطفال. كما نظمت الشركة ورشة لتعليم مباديء السباكة الأولية، وألقت ندوة عن أهمية المياه للسيدات بالقرية. وقام فريق التوعية بالمرور على المواطنين في منازلهم وبالشوارع لإعلامهم بأنشطة القافلة، وضرورة ترشيد المياه.<br /> كما حرصت الشركة على تلقي شكاوى المواطنين بالقرية، وذلك من خلال تواجدها بمقر القافلة الثابتة بدار مناسبات مسجد الشهداء، وتم حل أغلب الشكاوى على الفور على مدار اليومان.<br /> وصاحب القافلة ثلاث فرق من معامل الشركة، وقاموا بأخذ عينات عشوائية للمياه بجميع أنحاء القرية، وتم تحليلها للتأكد من سلامة المياه بالقرية، وجاءت جميع العينات مطابقة للمواصفات القياسية. وقاموا بتطهير خزانات المواطنين بالقرية بشكل مجاني، وسيستمر التطهير لكل المواطنين الذين قاموا بطلب التطهير على مدار الأسبوع، من قبل فرق معامل الشركة.<br /> وقامت الشركة بتطهير البيارات وغرف التفتيش بشوارع القرية ، بالإضافة لإصلاح كسر بخط مياه قطر ٦ بوصة اسبستوس، حيث تم استبدال جزء من الخط بطول متر ونصف، وتركيب ٢ جيوبلت في شارع داير الناحية بجوار مسجد سيدي محمد بيبرس.<br /> والجدير بالذكر أن الشركة حرصت على عمل استطلاع رأي قبلي لأهالي القرية؛ لمعرفة طبيعة المشكلات الموجودة بالقرية بقطاعي مياه الشرب والصرف الصحي.</p>



